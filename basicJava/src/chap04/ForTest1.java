package chap04;
//for문의 기본문법

public class ForTest1 {

	public static void main(String[] args) {
		
		for(int i=1; i<=5;i++) {
			System.out.println("자바프로그래밍");
		}
		System.out.println("==========================");
		
		for(int i =10;i<15;i++) {
			System.out.println("자바프로그래밍");
		}
		System.out.println("==========================");
		for(int i=1;i<10;i=i+2) {
			System.out.println("자바프로그래밍");
		}
		System.out.println("==========================");
		for(int i=5;i>=1;i--) {
			System.out.println("자바프로그래밍");
		}
		
	}

}
